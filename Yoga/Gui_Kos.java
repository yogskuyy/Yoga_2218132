    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
        // Koneksi ke database
        koneksi();

        // Mendapatkan indeks baris yang dipilih
        int selectedRow = tabel.getSelectedRow();

        // Jika tidak ada baris yang dipilih, munculkan pesan dan keluar dari metode
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Pilih baris yang ingin dihapus");
            return;
        }

        // Mendapatkan nilai nama dari baris yang dipilih
        String selectedNama = (String) tabel.getValueAt(selectedRow, 0);

        // Query SQL untuk menghapus data dari tabel Penghuni berdasarkan nama
        String sql = "DELETE FROM Penghuni WHERE Nama = ?";

        // Membuat objek PreparedStatement untuk eksekusi query
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            // Mengisi parameter query dengan nilai nama yang dipilih
            ps.setString(1, selectedNama);

            // Mengeksekusi query
            int result = ps.executeUpdate();

            // Menampilkan pesan berhasil atau gagal
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Data di Database berhasil dihapus");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Menghapus Data di Database");
            }
        }

        // Menutup koneksi
        conn.close();

        // Menghapus baris yang dipilih dari tabel GUI
        DefaultTableModel dataModel = (DefaultTableModel) tabel.getModel();
        dataModel.removeRow(selectedRow);

    } catch (SQLException ex) {
        Logger.getLogger(Gui_Kos.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
         try {
        // Koneksi ke database
        koneksi();

        // Query SQL untuk mencari data berdasarkan nama
        String sql = "SELECT * FROM Penghuni WHERE Nama LIKE ?";

        // Membuat objek PreparedStatement untuk eksekusi query
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            // Mengisi parameter query dengan nilai nama yang dicari
            ps.setString(1, "%" + txtCari.getText() + "%");

            // Mengeksekusi query
            ResultSet rs = ps.executeQuery();

            // Membersihkan data di tabel GUI
            DefaultTableModel dataModel = (DefaultTableModel) tabel.getModel();
            dataModel.setRowCount(0);

            // Menampilkan hasil pencarian di tabel GUI
            while (rs.next()) {
                Object[] rowData = {
                    rs.getString("Nama"),
                    rs.getString("Jenis_Kamar"),
                    rs.getString("Jenis_Kasur"),
                    rs.getString("Jenis_Lemari"),
                    rs.getString("Fasilitas"),
                    rs.getString("Tanggal_Login"),
                    rs.getDouble("Harga_Kamar")
                };
                dataModel.addRow(rowData);
            }

            // Jika tidak ada hasil pencarian, munculkan pesan
            if (dataModel.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            }
        }

        // Menutup koneksi
        conn.close();

    } catch (SQLException ex) {
        Logger.getLogger(Gui_Kos.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnCariActionPerformed

