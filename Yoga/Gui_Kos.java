    private void btnCtkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCtkActionPerformed
       try {
        // Koneksi ke database
        koneksi();

        // Query SQL untuk mengecek apakah data dengan nama yang sama sudah ada
        String cekDuplikat = "SELECT COUNT(*) FROM Penghuni WHERE Nama = ?";
        try (PreparedStatement psCek = conn.prepareStatement(cekDuplikat)) {
            psCek.setString(1, txtNama.getText());
            ResultSet rs = psCek.executeQuery();
            rs.next();
            int count = rs.getInt(1);

            // Jika nama sudah ada, munculkan pesan dan keluar dari metode
            if (count > 0) {
                JOptionPane.showMessageDialog(null, "Data dengan nama yang sama sudah ada. Silakan masukkan nama lain.");
                return;
            }
        }

        // Query SQL untuk menyimpan data ke tabel Penghuni
        String sql = "INSERT INTO Penghuni (Nama, Jenis_Kamar, Jenis_Kasur, Jenis_Lemari, Fasilitas, Tanggal_Login, Harga_Kamar) VALUES (?, ?, ?, ?, ?, ?, ?)";

        // Membuat objek PreparedStatement untuk eksekusi query
        try (PreparedStatement ps = conn.prepareStatement(sql)) {
            // Mengisi parameter query dengan nilai dari inputan pengguna
            ps.setString(1, txtNama.getText());
            if (btnVIP.isSelected()) {
                ps.setString(2, "VIP");
            } else {
                ps.setString(2, "Regular");
            }
            ps.setString(3, txtKasur.getText());
            ps.setString(4, txtLemari.getText());
            ps.setString(5, txtFasilitas.getText());
            ps.setString(6, txtTgl.getText());
            ps.setDouble(7, Double.parseDouble(txtHarga.getText()));

            // Mengeksekusi query
            int result = ps.executeUpdate();

            // Menampilkan pesan berhasil atau gagal
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Data Anda Ditambahkan ke Tabel dan Database");
            } else {
                JOptionPane.showMessageDialog(null, "Gagal Menyimpan Data ke Database");
            }
        }

        // Menutup koneksi
        conn.close();

        // Menampilkan data di tabel GUI setelah disimpan
        DefaultTableModel dataModel = (DefaultTableModel) tabel.getModel();
        List<Object> list = new ArrayList<>();
        list.add(txtNama.getText());
        if (btnVIP.isSelected()) {
            list.add("VIP");
        } else {
            list.add("Regular");
        }
        list.add(txtKasur.getText());
        list.add(txtLemari.getText());
        list.add(txtFasilitas.getText());
        list.add(txtTgl.getText());
        list.add(Double.parseDouble(txtHarga.getText()));
        dataModel.addRow(list.toArray());

    } catch (SQLException ex) {
        Logger.getLogger(Gui_Kos.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnCtkActionPerformed

