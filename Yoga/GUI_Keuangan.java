    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
       try {
        koneksi(); // Membuat koneksi ke database

        // Mendapatkan nilai dari inputan
        String bulan = jComboBox1.getSelectedItem().toString();
        int anakVIP = Integer.parseInt(txtVIP.getText());
        int anakRegular = Integer.parseInt(txtReg.getText());

        // Harga per anak untuk VIP dan Regular
        int hargaAnakVIP = 2500000;
        int hargaAnakRegular = 1000000;

        // Harga untuk air, listrik, dan wifi
        double hargaAir = Double.parseDouble(txtAir.getText());
        double hargaListrik = Double.parseDouble(txtListrik.getText());
        double hargaWifi = Double.parseDouble(txtWifi.getText());

        // Melakukan perhitungan
        double uangPerawatan = Double.parseDouble(txt_perawatan.getText());
        double jmlPendapatan = (anakVIP * hargaAnakVIP) + (anakRegular * hargaAnakRegular);
        double uangAir = hargaAir;
        double uangListrik = hargaListrik;
        double uangWifi = hargaWifi;
        double totalPengeluaran = uangAir + uangListrik + uangWifi + uangPerawatan;
        double sisaSaldo = jmlPendapatan - totalPengeluaran;

        // Menyimpan data ke database
        String query = "INSERT INTO PendapatanPengeluaran (Bulan, Jumlah_VIP, Jumlah_Regular, Uang_Air, Uang_Listrik, Jumlah_Lantai_Kos, Uang_Wifi, Uang_Perawatan, Jml_Pendapatan, Total_Pengeluaran, Sisa_Saldo) "
                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        try (PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, bulan);
            pstmt.setInt(2, anakVIP);
            pstmt.setInt(3, anakRegular);
            pstmt.setDouble(4, uangAir);
            pstmt.setDouble(5, uangListrik);
            pstmt.setInt(6, Integer.parseInt(txtLantai.getText())); // Anggap Jumlah_Lantai_Kos bertipe INT
            pstmt.setDouble(7, uangWifi);
            pstmt.setDouble(8, uangPerawatan);
            pstmt.setDouble(9, jmlPendapatan);
            pstmt.setDouble(10, totalPengeluaran);
            pstmt.setDouble(11, sisaSaldo);

            int result = pstmt.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal disimpan ke database");
            }
        }

        // Menampilkan hasil di tabel
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.addRow(new Object[]{bulan, anakVIP, anakRegular, uangAir, uangListrik, Integer.parseInt(txtLantai.getText()), uangWifi, uangPerawatan, jmlPendapatan, totalPengeluaran, sisaSaldo});

        clear(); // Membersihkan inputan setelah cetak
    } catch (SQLException | NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnCetakActionPerformed

