    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
        try {
        koneksi(); // Membuat koneksi ke database

        // Mendapatkan nilai dari inputan
        String bulanCari = comboCari.getSelectedItem().toString();

        // Melakukan pencarian data di database
        String query = "SELECT * FROM PendapatanPengeluaran WHERE Bulan = ?";
        try (PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, bulanCari);

            ResultSet rs = pstmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            model.setRowCount(0); // Menghapus semua baris di tabel

            while (rs.next()) {
                String bulan = rs.getString("Bulan");
                int jumlahVIP = rs.getInt("Jumlah_VIP");
                int jumlahRegular = rs.getInt("Jumlah_Regular");
                double uangAir = rs.getDouble("Uang_Air");
                double uangListrik = rs.getDouble("Uang_Listrik");
                int jumlahLantai = rs.getInt("Jumlah_Lantai_Kos");
                double uangWifi = rs.getDouble("Uang_Wifi");
                double uangPerawatan = rs.getDouble("Uang_Perawatan");
                double jmlPendapatan = rs.getDouble("Jml_Pendapatan");
                double totalPengeluaran = rs.getDouble("Total_Pengeluaran");
                double sisaSaldo = rs.getDouble("Sisa_Saldo");

                model.addRow(new Object[]{bulan, jumlahVIP, jumlahRegular, uangAir, uangListrik, jumlahLantai, uangWifi, uangPerawatan, jmlPendapatan, totalPengeluaran, sisaSaldo});
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btncariActionPerformed

    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        // TODO add your handling code here:
        try {
        // Mendapatkan indeks baris terpilih
        int selectedRow = tabel.getSelectedRow();

        // Pastikan ada baris yang dipilih
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris terlebih dahulu");
            return;
        }

        // Mendapatkan nilai dari kolom Bulan pada baris terpilih
        String bulanHapus = tabel.getValueAt(selectedRow, 0).toString();

        koneksi(); // Membuat koneksi ke database

        // Melakukan penghapusan data di database
        String query = "DELETE FROM PendapatanPengeluaran WHERE Bulan = ?";
        try (PreparedStatement pstmt = conn.prepareStatement(query)) {
            pstmt.setString(1, bulanHapus);

            int result = pstmt.executeUpdate();
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus dari database");
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal dihapus dari database");
            }
        }

        // Menghapus baris terpilih dari tabel
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.removeRow(selectedRow);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btn_hapusActionPerformed

